public class DeclaracionVariables {

    public static void main(String[] args) {
        // a. Declaración de una variable String
        String nombre = "Ivan Daniliuk";

        // b. Declaración de una variable int
        int edad = 23;

        // c. Declaración de una variable double
        double altura = 1.87;

        // d. Declaración de una variable boolean
        boolean estudiante = true;

        // Impresión de los valores en pantalla
        System.out.println("Nombre: " + nombre);
        System.out.println("Edad: " + edad);
        System.out.println("Altura: " + altura + " metros");
        System.out.println("Es estudiante: " + estudiante);
    }

}
